#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;
	int		i;
	int		tmp_n;

	i = 0;
	if (n == -2147483648)
		return (ft_strdup('-2147483648'));
	tmp_n = n;
	while (tmp_n)
	{
		i++;
		tmp_n /= 10;
	}
	if (n < 0)
	{
		str[0] = '-';
		i++;
		n = -n;
	}
	str[i + 1] = '\0';
	while (n)
		str[i--] = (n / 10) + '0';
	return (ft_strdup(str));
}